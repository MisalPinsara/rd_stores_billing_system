    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        boolean result;
        if(jTable3 != null){
        if(!jTable2.getSelectionModel().isSelectionEmpty()){
            String stringQty = JOptionPane.showInputDialog(this, "Enter the quantity");
            if(stringQty != null && !stringQty.isEmpty()){
                boolean isDouble = Controller.isDouble(stringQty);
                if(isDouble == true){
                    double itemQty = Double.parseDouble(stringQty);
                    int rowIndex = jTable2.getSelectedRow();
                    int  colCount = jTable2.getColumnCount();
                    Object[] rowData = new Object[colCount];
                    for(int i = 0; i<colCount; i++){
                        rowData[i] = jTable2.getValueAt(rowIndex, i);
                    }
                    Object[] cartData = Controller.getItemRowData(rowData, itemQty);
                    
                    int tempIDtoCart = Integer.parseInt(rowData[0].toString());
                    DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
                    boolean ifExist = false;
                    for(int row = 0; row<model.getRowCount(); row++){
                            int tempIDinCart = Integer.parseInt(model.getValueAt(row, 0).toString());
                            if(tempIDinCart == tempIDtoCart){
                                ifExist = true;
                                break;
                            }
                        }
                        if(ifExist){
                            JOptionPane.showMessageDialog(this, "item is already in the cart, remove to add again");
                        } else {
                            loadToCart(cartData);
                            cartTotalHolder();
                        }
                } else {
                    JOptionPane.showMessageDialog(this, "Unit price must be a number");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Please enter a quantity");
            }
        } else {
            JOptionPane.showMessageDialog(this, "select an item from the table to add");
        }
        } else {
            JOptionPane.showMessageDialog(this, "creat a cart first");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

