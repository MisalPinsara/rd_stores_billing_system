    public void loadData() {
        List<List<Object>> allRows = Controller.itemLister();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        DefaultTableModel model2 = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);
        model2.setRowCount(0);
        
        for(List<Object> row : allRows){
            model.addRow(row.toArray());
            model2.addRow(row.toArray());
        }
    }
    
    public void loadToCart(Object[] cartData) {
        DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
        model.addRow(cartData);
    }
    
    public void cartTotalHolder(){
        DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
        int colIndex = model.getColumnCount() - 1;
        double value = 0;
        double[] values = new double[model.getRowCount()];
        for(int rowIndex = 0; rowIndex < model.getRowCount(); rowIndex++){
            String stringValue = model.getValueAt(rowIndex, colIndex).toString();
            value = Double.parseDouble(stringValue);
            values[rowIndex] = value;
        }
        String total = Controller.getTotal(values);
        jLabel2.setText(total);
    }
    
